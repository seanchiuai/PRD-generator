import { NextRequest, NextResponse } from 'next/server'
import { anthropic, AI_MODELS, TOKEN_LIMITS } from "@/lib/ai-clients";
import {
  handleAPIError,
  handleValidationError,
  handleUnauthorizedError,
} from "@/lib/api-error-handler";
import { safeParseAIResponse } from "@/lib/parse-ai-json";
import { getAuthenticatedConvexClient } from "@/lib/convex-client";
import { api } from '@/convex/_generated/api'
import { getDefaultTechStack, generateMockResearchResults } from '@/lib/techStack/defaults'
import { Id } from "@/convex/_generated/dataModel";
import { withAuth } from "@/lib/middleware/withAuth";

export const POST = withAuth(async (request, { userId, token }) => {
  try {
    const { conversationId, useAI = false } = await request.json()

    if (!conversationId) {
      return handleValidationError("Conversation ID required");
    }

    // Get authenticated Convex client
    const convexClient = getAuthenticatedConvexClient(token);

    // Fetch conversation data
    const conversation = await convexClient.query(api.conversations.get, {
      conversationId: conversationId as Id<"conversations">,
    })

    if (!conversation) {
      return handleAPIError(
        new Error("Conversation not found"),
        "find conversation",
        404
      );
    }

    // Verify ownership
    if (conversation.userId !== userId) {
      return handleUnauthorizedError();
    }

    const extractedContext = conversation.extractedContext
    const clarifyingQuestions = conversation.clarifyingQuestions

    let techStack

    if (useAI && extractedContext) {
      // Use Claude for smarter suggestions
      techStack = await getAISuggestedStack(extractedContext, clarifyingQuestions)
    } else {
      // Use rule-based defaults
      techStack = getDefaultTechStack(extractedContext, clarifyingQuestions)
    }

    // Generate mock research results
    const researchResults = generateMockResearchResults(techStack)

    // Validate the stack before saving
    const validation = validateDefaultStack(techStack)

    // If there are errors, fix them before saving
    if (validation.errors.length > 0) {
      techStack = await fixStackErrors(techStack, validation.errors)
    }

    // Ensure all required fields are present for Convex mutation
    const completeStack = {
      frontend: techStack.frontend || "Next.js",
      backend: techStack.backend || "Node.js with Express",
      database: techStack.database || "PostgreSQL",
      auth: techStack.auth || "Clerk",
      hosting: techStack.hosting || "Vercel",
    }

    // Save research results to Convex
    await convexClient.mutation(api.conversations.saveResearchResults, {
      conversationId,
      results: researchResults,
      autoGenerated: true,
    })

    // Save validated/fixed selection once
    await convexClient.mutation(api.conversations.saveSelection, {
      conversationId,
      selection: completeStack,
      autoSelected: true,
    })

    return NextResponse.json({
      success: true,
      techStack,
      validation,
    })
  } catch (error) {
    return handleAPIError(error, "generate default tech stack");
  }
});

async function getAISuggestedStack(extractedContext: any, answers: any) {
  const prompt = `
Suggest an optimal tech stack for this product:

PRODUCT CONTEXT:
${JSON.stringify(extractedContext, null, 2)}

ANSWERS:
${JSON.stringify(answers, null, 2)}

Based on this information, suggest:
1. Frontend framework/library
2. Backend framework/language
3. Database
4. Authentication solution
5. Hosting platform

Consider:
- Product type and scale
- Target audience
- Technical preferences mentioned
- Industry best practices
- Developer experience
- Cost-effectiveness

Return ONLY a JSON object:
{
  "frontend": "technology name",
  "backend": "technology name",
  "database": "technology name",
  "auth": "technology name",
  "hosting": "technology name"
}
`

  const response = await anthropic.messages.create({
    model: AI_MODELS.CLAUDE_SONNET,
    max_tokens: TOKEN_LIMITS.TECH_STACK,
    temperature: 0.3,
    messages: [{ role: 'user', content: prompt }],
  })

  const textContent = response.content.find((block) => block.type === 'text')
  if (!textContent || textContent.type !== 'text') {
    throw new Error('Unexpected response type from Claude')
  }

  interface TechStack {
    frontend: string;
    backend: string;
    database: string;
    auth: string;
    hosting: string;
  }

  return safeParseAIResponse<TechStack>(textContent.text) || getDefaultTechStack(extractedContext, answers)
}

interface TechStack {
  frontend?: string;
  backend?: string;
  database?: string;
  auth?: string;
  hosting?: string;
}

interface ValidationResult {
  isValid: boolean;
  errors: string[];
  warnings: string[];
}

function validateDefaultStack(stack: TechStack): ValidationResult {
  const errors: string[] = [];
  const warnings: string[] = [];

  // Validate required fields exist and are non-empty
  if (!stack.frontend || stack.frontend.trim() === "") {
    errors.push("Frontend framework is required");
  }
  if (!stack.backend || stack.backend.trim() === "") {
    errors.push("Backend framework is required");
  }
  if (!stack.database || stack.database.trim() === "") {
    errors.push("Database is required");
  }
  if (!stack.auth || stack.auth.trim() === "") {
    errors.push("Authentication solution is required");
  }
  if (!stack.hosting || stack.hosting.trim() === "") {
    errors.push("Hosting platform is required");
  }

  // Basic compatibility checks
  if (stack.frontend && stack.hosting) {
    // Warn if using React/Next.js but not deploying to Vercel
    if (
      (stack.frontend.toLowerCase().includes("next") ||
        stack.frontend.toLowerCase().includes("react")) &&
      !stack.hosting.toLowerCase().includes("vercel")
    ) {
      warnings.push(
        "Consider Vercel for Next.js/React apps for optimal DX"
      );
    }
  }

  // Warn about potential compatibility issues
  if (stack.backend && stack.database) {
    // Example: MongoDB works best with Node.js
    if (
      stack.database.toLowerCase().includes("mongo") &&
      !stack.backend.toLowerCase().includes("node")
    ) {
      warnings.push(
        "MongoDB is typically used with Node.js backends"
      );
    }
  }

  return {
    isValid: errors.length === 0,
    errors,
    warnings,
  };
}

async function fixStackErrors(
  stack: TechStack,
  errors: string[]
): Promise<TechStack> {
  // Create a copy to fix
  const fixedStack = { ...stack };

  // Fix missing required fields with safe defaults
  errors.forEach((error) => {
    if (error.includes("Frontend")) {
      fixedStack.frontend = "Next.js";
    }
    if (error.includes("Backend")) {
      fixedStack.backend = "Node.js with Express";
    }
    if (error.includes("Database")) {
      fixedStack.database = "PostgreSQL";
    }
    if (error.includes("Authentication")) {
      fixedStack.auth = "Clerk";
    }
    if (error.includes("Hosting")) {
      fixedStack.hosting = "Vercel";
    }
  });

  return fixedStack;
}
